    @SuppressWarnings("unchecked")
    public void setValueToTable(ArrayList<StudentGrade> GradeList) throws SQLException {

        DefaultTableModel model = (DefaultTableModel) tbGrade.getModel();
        model.setRowCount(0);
        for (int i = 0; i < GradeList.size(); i++) {
            Vector row = new Vector();
            row.add(GradeList.get(i).getEnrollment());
            Course course = courseBUS.readCourseByID(GradeList.get(i).getCourseID());
            String nameCourse = course.getCourseID() + "-" + course.getTitle();
            row.add(nameCourse);

            Person person = personBUS.readStudentByID(GradeList.get(i).getStudentID());
            //System.out.println(GradeList.get(i).getStudentID());
            String studentName = person.getPersonID() + "-" + person.getFirstName() + " " + person.getLastName();
            //row.add(GradeList.get(i).getStudentID());
            row.add(studentName);
            row.add(GradeList.get(i).getGrade());
            model.addRow(row);

        }
        tbGrade.setModel(model);
    }

