    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked

        int i = tbGrade.getSelectedRow();
        if (i != -1) {
            try {
                int id = Integer.parseInt(tbGrade.getModel().getValueAt(i, 0).toString());
                String[] course = txtCourseHelp.getText().split("-");
                int courseID = Integer.parseInt(course[0]);

                String[] student = txtStudentHelp.getText().split("-");
                int studentID = Integer.parseInt(student[0]);

                StudentGrade stu = new StudentGrade(id, courseID, studentID, Double.parseDouble(txtGrade.getText()));
                if (gradeBUS.updateStu(stu) > 0) {

                    JOptionPane.showMessageDialog(this, "Updated Successfully", "Notify", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    loadAllGrade();
                } else {
                    JOptionPane.showMessageDialog(this, "Error, Update Fail", "Notify", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error" + ex.getMessage(), "Notify", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error, Plese select a record you want to update in table first", "Notify", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditMouseClicked

    private void btnDelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDelMouseClicked

        int i = tbGrade.getSelectedRow();
        if (i != -1) {

            int choice = JOptionPane.showConfirmDialog(this, "Are you sure want to delete it", "Confirm Dialog", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                try {
                    int id = Integer.parseInt(tbGrade.getModel().getValueAt(i, 0).toString());

                    if (gradeBUS.deleteStudentGrade(id) > 0) {
                        JOptionPane.showMessageDialog(this, "Deleted Successfully", "Notify", JOptionPane.INFORMATION_MESSAGE);
                        loadAllGrade();

                    } else {
                        JOptionPane.showMessageDialog(this, "Deleted Fail", "Notify", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(CourseGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please, choose record you want to delete first", "Notify", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnDelMouseClicked

