    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked

        try {
            String[] course = txtCourseHelp.getText().split("-");
            int courseID = Integer.parseInt(course[0]);

            String[] student = txtStudentHelp.getText().split("-");
            int studentID = Integer.parseInt(student[0]);

            double grade = Double.parseDouble(txtGrade.getText());

            StudentGrade stu = new StudentGrade(courseID, studentID, grade);

            if (gradeBUS.insertStudentGrade(stu) > 0) {
                JOptionPane.showMessageDialog(this, "Inserted Successfully", "Notify", JOptionPane.INFORMATION_MESSAGE);
                reset();
                loadAllGrade();

            } else {
                JOptionPane.showMessageDialog(this, "Error, Inserted Fail", "Notify", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Notify", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddMouseClicked

